    private void jbCrearEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCrearEmpleadoActionPerformed
        System.out.println("Creando empleado...");
        String nombre = jtfNombre.getText();
        String apellido1 = jtfApellido1.getText();
        String apellido2 = jtfApellido2.getText();
        String puesto = jtfPuesto.getText();
        Integer salario = Integer.parseInt(jtfSalario.getText());

        Empleado nuevoEmpleado = new Empleado(nombre, apellido1, apellido2, puesto, salario);
        GestorPersistencia gp = null;
        try {
            gp = new GestorPersistencia();
            gp.add(nuevoEmpleado);
            gp.doCommit();
            JOptionPane.showMessageDialog(this, "El empleado ha sido dado de alta satisfactoriamente");
            limpiarFormulario();
            recuperarEmpleados();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Ha ocurrido un error en la base de datos",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            try {
                gp.doRollback();//ROLLBACK SI HAY ERROR
            } catch (SQLException exc) {
                exc.printStackTrace();
                JOptionPane.showMessageDialog(this,
                        "Ha ocurrido un error en la base de datos",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Ha ocurrido un error en la base de datos",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                gp.closeConnection();//CLOSECONNECTION SIEMPRE
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }

    }//GEN-LAST:event_jbCrearEmpleadoActionPerformed

