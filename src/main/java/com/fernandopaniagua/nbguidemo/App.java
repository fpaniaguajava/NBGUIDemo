    void recuperarEmpleados() { 
        System.out.println("Recuperando empleados...");
        try {
            GestorPersistencia gp = new GestorPersistencia();
            ArrayList<Empleado> empleados = gp.findAll();
            TablaEmpleados te = new TablaEmpleados(empleados);
            jtableEmpleados.setModel(te);
            gp.closeConnection();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Ha ocurrido un error",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
